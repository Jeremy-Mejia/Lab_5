#include <Arduino.h>
#include <LiquidCrystal.h>

//***************************************************************************************************************
// Definición de Pines
//***************************************************************************************************************

//Definición de entradas
#define Pot1 14 //Potenciómetro 1
#define Pot2 27 //Potenciómetro 2

//Definición de pines de salidas LCD
#define RS 2
#define E 16
#define D4 21
#define D5 3
#define D6 1
#define D7 22

//***************************************************************************************************************
// Prototipo de Funciones
//***************************************************************************************************************


//***************************************************************************************************************
// Variables Globales
//***************************************************************************************************************
int contador = 0;
LiquidCrystal LCD(RS, E, D4, D5, D6, D7);
int adcRaw;
int voltaje; 
int voltaje1; 
//***************************************************************************************************************
// ISR (Interrupciones)
//***************************************************************************************************************

//***************************************************************************************************************
// Configuración
//***************************************************************************************************************

void setup() {
  LCD.begin(16, 2);
}

//***************************************************************************************************************
// Loop Principal
//***************************************************************************************************************

void loop() {
  voltaje = analogRead(Pot1);
  voltaje = map(voltaje, 0, 4095, 0, 255);

  voltaje1 = analogRead(Pot2);
  voltaje1 = map(voltaje1, 02, 4095, 0, 255);

  LCD.clear();
  LCD.print("Rojo ");
  LCD.print("Verde ");
  LCD.print("Azul ");
  LCD.setCursor(0, 1);
  LCD.print(voltaje);
  
  LCD.setCursor(6, 1);
  LCD.print(voltaje1);

  delay(250);
}

